            <h2>Parthia<h2>
                    <h3>Basic Information</h3>
                    <p>The Parthian Empire (/ˈpɑːrθiən/; 247 BC – 224 AD), also known as the Arsacid Empire
                        (/ˈɑːrsəsɪd/), was a major Iranian political and cultural power in ancient Iran. Its
                        latter name comes from Arsaces I of Parthia who, as leader of the Parni tribe,
                        founded it in the mid-3rd century BC when he conquered the region of Parthia in
                        Iran's northeast, then a satrapy (province) under Andragoras, in rebellion against
                        the Seleucid Empire. Mithridates I of Parthia (r. c. 171–138 BC) greatly expanded
                        the empire by seizing Media and Mesopotamia from the Seleucids.</p>

                    <h3>History</h3>
                    <p>At its height, the Parthian Empire stretched from the northern reaches of the
                        Euphrates, in what is now central-eastern Turkey, to eastern Iran. The empire,
                        located on the Silk Road trade route between the Roman Empire in the Mediterranean
                        Basin and the Han Empire of China, became a center of trade and commerce. The
                        earliest enemies of the Parthians were the Seleucids in the west and the Scythians
                        in the east. However, as Parthia expanded westward, they came into conflict with the
                        Kingdom of Armenia, and eventually the late Roman Republic. Rome and Parthia
                        competed with each other to establish the kings of Armenia as their subordinate
                        clients. The Parthians soundly defeated Marcus Licinius Crassus at the Battle of
                        Carrhae in 53 BC, and in 40–39 BC, Parthian forces captured the whole of the Levant
                        except Tyre from the Romans. However, Mark Antony led a counterattack against
                        Parthia, although his successes were generally achieved in his absence, under the
                        leadership of his lieutenant Ventidius. </p>

                    <h3>Conflict with Rome</h3>
                    <p>Various Roman emperors or their appointed generals invaded Mesopotamia in the course
                        of the ensuing Roman-Parthian Wars of the next few centuries. The Romans captured
                        the cities of Seleucia and Ctesiphon on multiple occasions during these conflicts,
                        but were never able to hold on to them. Frequent civil wars between Parthian
                        contenders to the throne proved more dangerous to the Empire's stability than
                        foreign invasion, and Parthian power evaporated when Ardashir I, ruler of Istakhr in
                        Persis, revolted against the Arsacids and killed their last ruler, Artabanus V, in
                        224 AD. Ardashir established the Sassanid Empire, which ruled Iran and much of the
                        Near East until the Muslim conquests of the 7th century AD, although the Arsacid
                        dynasty lived on through the Arsacid Dynasty of Armenia, the Arsacid dynasty of
                        Iberia, and the Arsacid Dynasty of Caucasian Albania; all eponymous branches of the
                        Parthian Arsacids.</p>
