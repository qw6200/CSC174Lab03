            <h2>Hannibal Barca<h2>
                    <h3>Basic Information</h3>
                    <p>Hannibal Barca (/ˈhænɪbəl/; 247 – 183 or 181 BC) was a general of the Phoenician-Canaanite state
                        of
                        Ancient Carthage, and is widely considered as one of the greatest military commanders in
                        history.
                        His father Hamilcar Barca was the leading Carthaginian commander during the First Punic War. His
                        younger brothers were Mago and Hasdrubal, and he was brother-in-law to Hasdrubal the Fair.</p>

                    <h3>History</h3>
                    <p>Hannibal lived during a period of great tension in the western Mediterranean Basin, when the
                        Roman
                        Republic established its supremacy over other great powers such as ancient Carthage, the
                        Etruscans,
                        the Samnites, and the Greek kingdom of Syracuse. One of his most famous achievements was at the
                        outbreak of the Second Punic War, when he marched an army which included war elephants from
                        Iberia
                        over the Pyrenees and the Alps into Italy. In his first few years in Italy, he won dramatic
                        victories at the Trebia, Lake Trasimene, and Cannae. He distinguished himself for his ability to
                        determine his and his opponent's respective strengths and weaknesses, and to plan battles
                        accordingly. Hannibal's well-planned strategies allowed him to conquer many allies of Rome.</p>

                    <h3>Conflict with Rome</h3>
                    <p>Hannibal occupied part of southern Italy for 15 years, but was unable to march on Rome. An enemy
                        counter-invasion of North Africa forced him to return to Carthage, where he was decisively
                        defeated
                        by Scipio Africanus at the Battle of Zama. Scipio had studied Hannibal's tactics and brilliantly
                        devised some of his own, and he finally defeated Rome's nemesis at Zama, having previously
                        driven
                        Hannibal's brother Hasdrubal out of the Iberian Peninsula.</p>
