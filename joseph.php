            <h2>The Gauls<h2>
                    <h3>Basic Information</h3>
                    <p>The Gauls (Latin: Galli, Ancient Greek: Γαλάται, Galátai) were a group of Celtic peoples of
                        West-Central Europe in the Iron Age and the Roman period (roughly from the 5th century BC to
                        the 5th century AD). The area they inhabited was known as Gaul. Their Gaulish language forms
                        the main branch of the Continental Celtic languages.</p>

                    <h3>History</h3>
                    <p>The Gauls emerged around the 5th century BC as the bearers of the La Tène culture north of
                        the Alps (spread across the lands between the Seine, Middle Rhine and upper Elbe). By the
                        4th century BC, they spread over much of what is now France, Belgium, Spain, Portugal,
                        Switzerland, Southern Germany, Austria, the Czech Republic and Slovakia by virtue of
                        controlling the trade routes along the river systems of the Rhône, Seine, Rhine, and Danube,
                        and they quickly expanded into Northern Italy, the Balkans, Transylvania and Galatia. Gaul
                        was never united under a single ruler or government, but the Gallic tribes were capable of
                        uniting their forces in large-scale military operations. They reached the peak of their
                        power in the early 3rd century BC. </p>

                    <h3>Conflict with Rome</h3>
                    <p>The rising Roman Republic after the end of the First Punic War increasingly put pressure on
                        the Gallic sphere of influence; the Battle of Telamon of 225 BC heralded a gradual decline
                        of Gallic power over the 2nd century, until the eventual conquest of Gaul in the Gallic Wars
                        of the **50s BC** by Julius Caesar. After this, Gaul became a province of the Roman Empire,
                        and the Gauls were ethnically and culturally largely assimilated into Latin (Roman settlers)
                        majority, losing their tribal identities by the end of the 1st century AD.</p>
        </div>

